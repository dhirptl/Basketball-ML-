#!/usr/bin/env python3
"""Export a Player-only YOLO dataset (class 0) from a multi-class bbox dataset."""

from __future__ import annotations

import argparse
import os
import shutil
import sys
from pathlib import Path

from . import config


def _read_split_paths(yaml_path: Path) -> dict[str, str]:
    """Parse train/val/test image paths from a minimal Ultralytics-style yaml (no PyYAML)."""
    splits: dict[str, str] = {}
    for raw in yaml_path.read_text(encoding="utf-8").splitlines():
        line = raw.split("#", 1)[0].strip()
        if not line or line.startswith("#"):
            continue
        for key in ("train", "val", "test"):
            if line.startswith(f"{key}:"):
                val = line.split(":", 1)[1].strip().strip("'\"")
                if val:
                    splits[key] = val
                break
    return splits


def _write_dataset_yaml(out_path: Path, split_paths: dict[str, str]) -> None:
    lines = [
        "# Generated by src.export_player_only_dataset — paths relative to this file.",
        "nc: 1",
        "names: ['Player']",
    ]
    for k in ("train", "val", "test"):
        if k in split_paths:
            lines.append(f"{k}: {split_paths[k]}")
    out_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def _image_stem_to_label_path(images_dir: Path, image_path: Path) -> Path:
    labels_dir = images_dir.parent / "labels"
    return labels_dir / (image_path.stem + ".txt")


def _filter_label_lines(text: str, player_class_id: int) -> str:
    kept: list[str] = []
    for line in text.splitlines():
        line = line.strip()
        if not line:
            continue
        parts = line.split()
        try:
            cls = int(float(parts[0]))
        except (ValueError, IndexError):
            continue
        if cls != player_class_id:
            continue
        parts[0] = "0"
        kept.append(" ".join(parts))
    return "\n".join(kept) + ("\n" if kept else "")


def _link_or_copy(src: Path, dst: Path, use_symlink: bool) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.exists() or dst.is_symlink():
        dst.unlink()
    if use_symlink:
        rel = os.path.relpath(src, start=dst.parent)
        dst.symlink_to(rel)
    else:
        shutil.copy2(src, dst)


def _process_split(
    dataset_root: Path,
    images_rel: str,
    out_root: Path,
    player_class_id: int,
    use_symlink: bool,
) -> tuple[int, int]:
    """Returns (num_images_linked, num_label_files_written)."""
    src_img_dir = (dataset_root / images_rel).resolve()
    if not src_img_dir.is_dir():
        return 0, 0
    split = images_rel.split("/")[0]
    out_img = out_root / split / "images"
    out_lbl = out_root / split / "labels"
    out_img.mkdir(parents=True, exist_ok=True)
    out_lbl.mkdir(parents=True, exist_ok=True)

    n_img = 0
    n_lbl = 0
    exts = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}
    for img_path in sorted(src_img_dir.iterdir()):
        if not img_path.is_file() or img_path.suffix.lower() not in exts:
            continue
        dst_img = out_img / img_path.name
        _link_or_copy(img_path, dst_img, use_symlink)
        n_img += 1

        src_lbl = _image_stem_to_label_path(src_img_dir, img_path)
        dst_lbl = out_lbl / (img_path.stem + ".txt")
        if src_lbl.is_file():
            body = _filter_label_lines(src_lbl.read_text(encoding="utf-8"), player_class_id)
        else:
            body = ""
        dst_lbl.write_text(body, encoding="utf-8")
        n_lbl += 1
    return n_img, n_lbl


def main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(description="Export Player-only YOLO labels (class 0) + dataset.yaml")
    p.add_argument(
        "--src-yaml",
        type=str,
        default=str(config.DEFAULT_PLAYER_DATA_YAML),
        help="Source Ultralytics data.yaml (multi-class)",
    )
    p.add_argument("--out-dir", type=str, required=True, help="Output dataset root (created)")
    p.add_argument("--player-class-id", type=int, default=config.PLAYER_CLASS_ID, help="Source class id for Player")
    p.add_argument(
        "--copy-images",
        action="store_true",
        help="Copy images instead of symlinks (default: symlink to save disk)",
    )
    args = p.parse_args(argv)

    src_yaml = Path(args.src_yaml).resolve()
    if not src_yaml.is_file():
        print(f"Source yaml not found: {src_yaml}", file=sys.stderr)
        return 1

    out_root = Path(args.out_dir).resolve()
    out_root.mkdir(parents=True, exist_ok=True)
    dataset_root = src_yaml.parent

    splits = _read_split_paths(src_yaml)
    if not splits:
        print(f"No train/val/test keys found in {src_yaml}", file=sys.stderr)
        return 1

    use_symlink = not bool(args.copy_images)
    total_img = 0
    total_lbl = 0
    out_split_paths: dict[str, str] = {}

    for key, rel in splits.items():
        rel = rel.replace("\\", "/")
        ni, nl = _process_split(dataset_root, rel, out_root, args.player_class_id, use_symlink)
        total_img += ni
        total_lbl += nl
        out_split_paths[key] = rel.split("/")[0] + "/images"

    out_yaml = out_root / "dataset.yaml"
    _write_dataset_yaml(out_yaml, out_split_paths)

    print(f"Wrote {out_yaml} (images linked={total_img}, labels={total_lbl}, symlink={use_symlink})")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
